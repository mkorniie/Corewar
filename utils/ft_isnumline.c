#include "../asm.h"

int		ft_isnumline(char *line)
{
	int i;

	i = -1;
	while (line[++i] != '\0')
	{
		if (!(ft_isdigit(line[i])))
			return (0);
	}
	if (i > 10)
		return (0);
	return (1);
}
